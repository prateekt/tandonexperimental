package api.tuxguitar.MelodyStructureMiner;

public class StringWrapper {
	
	private String str;
	
	public StringWrapper(String str) {
		this.str = str;
	}

	/**
	 * @return the str
	 */
	public String getStr() {
		return str;
	}

	/**
	 * @param str the str to set
	 */
	public void setStr(String str) {
		this.str = str;
	}
}
